num1 = int (input("num 1:"))
num2 = int (input("num 2:"))
operasion = input("selekt operasion(-,+,*,/):")
if operasion == '+':
    result = (num1 + num2)
    print("Result:", result)
if operasion == '-':
    result = (num1 - num2)
    print("Result:", result)
if operasion == '*':
    result = (num1 * num2)
    print("Result:", result)
if operasion == '/':
    result = (num1 / num2)
    print("Result:", result)